
// while loop
var i = 1; // initialization

while ( i<=5 ) { //condition
   document.write(i);
    i++; //update
}

document.write("End of while loop!")
document.write("Start of for loop!");
// for loop

for (var j = 1; j<=5 ; j++) {
    document.write(j);
}

for (var k = 5; k >=1; k-- ) {
    document.write(k);
}
