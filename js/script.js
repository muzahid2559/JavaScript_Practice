// Comparison Operator
document.write(4 == 4);
document.write(4 == 6);
document.write(4 == "4"); //value same
document.write(4 === "4"); //value same but type is not same
document.write(4 != 6);
document.write(4 !== "4");
document.write(4<5);

//Logical Operator ( ||, && )
document.write(true || true);
document.write(false || true);
document.write(false && true);
document.write(true && true);


//Conditional Operator
var a = (1<8)? "Hello" : "world";
document.write(a);

